package com.cidacs.rl.editor.pair;

public class ColumnPair {

    int number;
    double weight;
    double phonWeight;
    String indexA;
    String indexB;
    String renameA;
    String renameB;
    String type;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getPhonWeight() {
        return phonWeight;
    }

    public void setPhonWeight(double phonWeight) {
        this.phonWeight = phonWeight;
    }

    public String getIndexA() {
        return indexA;
    }

    public void setIndexA(String indexA) {
        this.indexA = indexA;
    }

    public String getIndexB() {
        return indexB;
    }

    public void setIndexB(String indexB) {
        this.indexB = indexB;
    }

    public String getRenameA() {
        return renameA;
    }

    public void setRenameA(String renameA) {
        this.renameA = renameA;
    }

    public String getRenameB() {
        return renameB;
    }

    public void setRenameB(String renameB) {
        this.renameB = renameB;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
